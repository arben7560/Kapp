export const CONTENT_REFS_BY_ID = Object.fromEntries(
  CONTENT_REFS.map((contentRef) => [contentRef.id, contentRef]),
) as Readonly<Partial<Record<string, ContentRef>>>;

export function getContentRef(id: string): ContentRef | undefined {
  return CONTENT_REFS_BY_ID[id];
}

export function getContentLinksForConcept(conceptId: GrammarConceptId) {
  return GRAMMAR_CONTENT_LINKS.filter((link) => link.conceptId === conceptId);
}
